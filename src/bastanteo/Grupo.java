package bastanteo;

public class Grupo {
	private String codigo;
	private String nombre;

	public Grupo(String codigo,String nombre)
	{
		this.codigo=codigo;
	this.nombre=nombre;
	}

	public String getNombre() {
		return nombre;
	}
	
	public String getCodigo() {
		return codigo;
	}
	
	
}
